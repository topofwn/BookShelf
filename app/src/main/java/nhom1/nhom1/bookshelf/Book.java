package nhom1.nhom1.bookshelf;

public class Book {
    public String Name;
    public String Description;
    public String Expired_date;
    public long cost;

    public String getName() {
        return Name;
    }

    public String getDescription() {
        return Description;
    }

    public String getExpired_date() {
        return Expired_date;
    }

    public long getCost() {
        return cost;
    }

}
